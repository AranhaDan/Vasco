function clickMenu() {
    if (hamenu.style.display == 'block') {
        hamenu.style.display = 'none'
    } else {hamenu.style.display = 'block'}
    }

function mudTama() {
        if (window.innerWidth >= 769) {
            hamenu.style.display = 'block'
        } else {
            hamenu.style.display = 'none'
        }
    }